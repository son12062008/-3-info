# กำหนดราคาสินค้าในรายการที่เลือกซื้อ
items = {
    "หมูสับ": 150.00,
    "เนื้ออกไก่": 105.00,
    "ไก่บ้าน": 120.00,
    "มาม่าต้มยำ": 6.50,
    "ข้าวสาร": 150.00,
    "น้ำตาล": 20.00,
    "ปลากระป๋องสามแม่ครัว": 10.00,
    "ซอสน้ำมันหอย": 18.00,
    "ผงชูรส": 10.25,
    "ไข่แผงคละเบอร์": 120.25,
    "ชาเขียว": 21.50,
    "Pepsi": 29.50,
    "กาแฟ": 15.75,
    "ขนมปังอบเนย": 19.00,
    "ชาไทย": 11.50,
    "น้ำเปล่า": 15.15,
    "น้ำแข็ง": 10.00
}

# คำนวณราคารวมทั้งหมด
total_price = sum(items.values())

# เงินเริ่มต้น
budget = 1000.00

# คำนวณเงินทอน
change = budget - total_price

# แสดงผลลัพธ์
print("รายการสินค้าที่เลือกซื้อ:")
for item, price in items.items():
    print(f"- {item}: {price:.2f} บาท")

print(f"\nราคารวมทั้งหมด: {total_price:.2f} บาท")
print(f"เงินที่จ่าย: {budget:.2f} บาท")
print(f"เงินทอน: {change:.2f} บาท")
